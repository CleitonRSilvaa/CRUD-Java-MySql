    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int listagemItensSelectedRow = this.jTbListaPCs.getSelectedRow();  
        if(listagemItensSelectedRow>=0)
        {
          
            int ID = Integer.parseInt(jTbListaPCs.getModel().getValueAt(listagemItensSelectedRow, 0).toString());
            String hd = jTbListaPCs.getModel().getValueAt(listagemItensSelectedRow, 2).toString();
            String processador = jTbListaPCs.getModel().getValueAt(listagemItensSelectedRow, 3).toString();
            
            objcComputador.setId(ID);
            objcComputador.setHd(hd);
            objcComputador.setProcessador(processador);
            
            //Passo o objeto para a tela de Cadastro informando que é alteração
            TelaCadastroComputador telaCadastroComputador = new TelaCadastroComputador(objcComputador);
                
            telaCadastroComputador.modoTela = "Alteração";
            //Exibir o JFrame
            telaCadastroComputador.setVisible(true);

        }else{
            JOptionPane.showMessageDialog(this, "Selecione um computador da tabela!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        int listagemItensSelectedRow = this.jTbListaPCs.getSelectedRow();
        if(listagemItensSelectedRow < 0) {
            JOptionPane.showMessageDialog(this,"Selecione um computador da Tabela");
        }else if(listagemItensSelectedRow>=0) {
            int id = Integer.parseInt(jTbListaPCs.getModel().getValueAt(listagemItensSelectedRow, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog (null, "Tem certeza de que deseja deletar o computador ID: "+id);
            if(dialogResult == JOptionPane.YES_OPTION){
                try {
                    objcComputador.setId(id);
                    if( ComputadorDAO.deletar(objcComputador)){
                        JOptionPane.showMessageDialog(this,"Deletado com sucesso");
                         CarregarListaComputador(); 
                    }    
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this,"Falha ao gravar no banco de dados\n"+ ex.getMessage(), 
                                                        "Aviso de Falha", JOptionPane.ERROR_MESSAGE);
                }
            }
        }        
    }//GEN-LAST:event_btnDeletarActionPerformed

