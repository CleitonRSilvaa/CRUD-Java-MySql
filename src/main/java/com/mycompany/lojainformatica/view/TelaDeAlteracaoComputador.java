    private void bntFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntFiltrarActionPerformed
        // TODO add your handling code here:
        String processador = txtFiltro.getText();
        
        if (!processador.isBlank() || !processador.isEmpty()) {
            objcComputador.setProcessador(processador);
            ArrayList<Computador> listaComputadores = ComputadorDAO.consultarCamputadorFiltro(processador);
            if (listaComputadores.size()>0) {
                DefaultTableModel  model = new DefaultTableModel();
                model.addColumn("ID");
                model.addColumn("MARCA");
                model.addColumn("HD");
                model.addColumn("PROCESSADOR");
                jTbListaPCs.setModel(model);
                for (Computador c : listaComputadores) {
                     model.addRow(new  Object[]{c.getId(),c.getMarca(),c.getHd(),c.getProcessador()});
                }
            }else{
             JOptionPane.showMessageDialog(this,"Nenhum PROCESSADOR com esse nome foi encontrado");
             CarregarListaComputador();
            }
            
        }
        
        
        
        
        
    }//GEN-LAST:event_bntFiltrarActionPerformed

    public  void CarregarListaComputador() {
        ArrayList<Computador> listaComputadors = ComputadorDAO.listaComputadors();
        DefaultTableModel  model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("MARCA");
        model.addColumn("HD");
        model.addColumn("PROCESSADOR");
        jTbListaPCs.setModel(model);
        for (Computador c : listaComputadors) {
            
             model.addRow(new  Object[]{c.getId(),c.getMarca(),c.getHd(),c.getProcessador()});
        }
              
    }
 
    
